public class Operations {
    public static void main(String[] args) {
        int a = 23;
        int b = 15;
        int sum = a + b;
        System.out.println(sum);
        int diff = a - b;
        System.out.println(diff);
        int mult = a * b;
        System.out.println(mult);
        float dev = (float)a / (float)b;
        System.out.println(dev);
    }
}
